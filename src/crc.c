void append_crc_16(
struct mbuf *bp)
{

  uint8 *p;
  int crc = 0;
  int n;
  struct mbuf *last_bp;

  if (!bp) return;
  for (; bp; bp = bp->next) {
    last_bp = bp;
    for (p = bp->data, n = bp->cnt; n; n--)
      crc = (crc >> 8) ^ Crc_16_table[(crc ^ *p++) & 0xff];
  }
  if (!(bp = alloc_mbuf(2))) return;
  bp->data[0] = crc;
  bp->data[1] = crc >> 8;
  bp->cnt = 2;
  last_bp->next = bp;
}

/*---------------------------------------------------------------------------*/

int check_crc_16(
struct mbuf *head)
{

  uint8 *p;
  int cnt = 0;
  int crc = 0;
  int n;
  struct mbuf *bp;

  for (bp = head; bp; bp = bp->next) {
    cnt += bp->cnt;
    for (p = bp->data, n = bp->cnt; n; n--)
      crc = (crc >> 8) ^ Crc_16_table[(crc ^ *p++) & 0xff];
  }
  if (crc || cnt < 3) return -1;
  cnt -= 2;
  for (bp = head; bp->cnt < cnt; bp = bp->next) cnt -= bp->cnt;
  bp->cnt = cnt;
  free_p(&bp->next);
  return 0;
}

