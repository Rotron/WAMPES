void append_crc_ccitt(
struct mbuf *bp)
{

  uint8 *p;
  int crc = 0xffff;
  int n;
  struct mbuf *last_bp;

  if (!bp) return;
  for (; bp; bp = bp->next) {
    last_bp = bp;
    for (p = bp->data, n = bp->cnt; n; n--)
      crc = (crc >> 8) ^ Crc_ccitt_table[(crc ^ *p++) & 0xff];
  }
  crc ^= 0xffff;
  if (!(bp = alloc_mbuf(2))) return;
  bp->data[0] = crc;
  bp->data[1] = crc >> 8;
  bp->cnt = 2;
  last_bp->next = bp;
}

/*---------------------------------------------------------------------------*/

int check_crc_ccitt(
const char *buf,
int cnt)
{
  int crc = 0xffff;

  for (; cnt > 0; cnt--)
    crc = (crc >> 8) ^ Crc_ccitt_table[(crc ^ *buf++) & 0xff];
  return (crc & 0xffff) == 0xf0b8;
}
